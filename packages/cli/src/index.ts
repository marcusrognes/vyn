const MAIN_TS = `import { createApp, $ } from "@vynjs/client";
import type { AppRouter } from "../_vyn.gen.ts";

type Todo  = { id: string; title: string; done: boolean };
type Theme = "system" | "light" | "dark";

const { rpc } = createApp<AppRouter>();

// ── Theme: system | light | dark ─────────────────────────────────
// Persists choice in localStorage. The inline script in index.html
// pre-paints to avoid a flash; this function keeps the runtime in
// sync and reacts to OS changes while in "system" mode.
const root        = document.documentElement;
const toggleEl    = $("#theme-toggle")  as HTMLButtonElement;
const themeLabel  = $("#theme-label")   as HTMLSpanElement;
const darkQuery   = matchMedia("(prefers-color-scheme: dark)");

function applyTheme(mode: Theme): void {
	root.dataset.theme = mode;
	const dark = mode === "dark" || (mode === "system" && darkQuery.matches);
	root.classList.toggle("dark", dark);
	themeLabel.textContent = mode.charAt(0).toUpperCase() + mode.slice(1);
	localStorage.setItem("theme", mode);
}

const initial: Theme = (localStorage.getItem("theme") as Theme | null) ?? "system";
applyTheme(initial);

toggleEl.addEventListener("click", () => {
	const order: Theme[] = ["system", "light", "dark"];
	const next = order[(order.indexOf(root.dataset.theme as Theme) + 1) % order.length];
	applyTheme(next);
});

darkQuery.addEventListener("change", () => {
	if (root.dataset.theme === "system") applyTheme("system");
});

// ── Todos: load + subscribe ──────────────────────────────────────
const listEl  = $("#todos")     as HTMLUListElement;
const formEl  = $("#new-form")  as HTMLFormElement;
const inputEl = $("#new-title") as HTMLInputElement;
const countEl = $("#count")     as HTMLSpanElement;

const items: Todo[] = await rpc.todos.list.query({});
render(items);

rpc.todos.watch.listen({}, {
	onValue: (todo: Todo & { _removed?: boolean }) => {
		if (todo._removed) {
			const i = items.findIndex((x) => x.id === todo.id);
			if (i >= 0) items.splice(i, 1);
		} else {
			const i = items.findIndex((x) => x.id === todo.id);
			if (i >= 0) items[i] = todo;
			else        items.push(todo);
		}
		render(items);
	},
});

formEl.addEventListener("submit", async (e) => {
	e.preventDefault();
	const title = inputEl.value.trim();
	if (!title) return;
	inputEl.value = "";
	await rpc.todos.add.mutate({ title });
});

listEl.addEventListener("change", async (e) => {
	const cb = e.target as HTMLInputElement;
	if (cb.type !== "checkbox") return;
	const id = (cb.closest("li") as HTMLElement).dataset.id!;
	await rpc.todos.toggle.mutate({ id });
});

listEl.addEventListener("click", async (e) => {
	const btn = (e.target as HTMLElement).closest(".remove") as HTMLButtonElement | null;
	if (!btn) return;
	const id = (btn.closest("li") as HTMLElement).dataset.id!;
	await rpc.todos.remove.mutate({ id });
});

function render(rows: Todo[]): void {
	listEl.innerHTML = rows.map(rowHtml).join("");
	for (const li of listEl.querySelectorAll<HTMLLIElement>("li")) {
		linkifyURLs(li.querySelector("span")!);
	}
	const left = rows.filter((r) => !r.done).length;
	countEl.textContent = \`\${left} of \${rows.length} left\`;
}

function rowHtml(t: Todo): string {
	return \`
		<li data-id="\${t.id}" class="\${t.done ? "done" : ""}">
			<label>
				<input type="checkbox" \${t.done ? "checked" : ""}>
				<span>\${escapeHTML(t.title)}</span>
			</label>
			<button class="remove" aria-label="Remove">×</button>
		</li>
	\`;
}

function escapeHTML(s: string): string {
	return s.replace(/[&<>"']/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]!));
}

function linkifyURLs(el: HTMLElement): void {
	const url = /https?:\\/\\/\\S+/g;
	el.innerHTML = el.innerHTML.replace(url, (u) => \`<a href="\${u}" target="_blank" rel="noopener">\${u}</a>\`);
}
`;

